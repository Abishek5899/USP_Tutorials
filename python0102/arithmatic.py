#!/bin/env python3

#real numbers arithmetics
x = 5
y = 2.5
print(">> Real numbers arithmetics: "+"x = "+str(x)+" and y = "+str(y))
print("x+y = "+str(x+y))
print("x-y = "+str(x-y))
print("x*y = "+str(x*y))   
print("x/2 = "+str(x/2))   #integer
print("y/x = "+str(y/x))   #decimal modulus
print("x%2 = "+str(x%2))   #integer modulus
print("x%y = "+str(x%y))   #decimal modulus
print("x**2 = "+str(x**2)) #power
print("x**y = "+str(x**y)) #decimal power
print("x//2 = "+str(x//2)) #floor division
print("x**y = "+str(x//y)) #decimal floor division
print()

#complex numbers arithmetics
x = 1+2j
